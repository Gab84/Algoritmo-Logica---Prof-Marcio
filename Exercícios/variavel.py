# Em Python, a necessidade de declarar explicitamente o tipo de dado de uma variável é dispensada por causa da tipagem dinâmica. 
# Declaração de variáveis
numero_inteiro = 10
numero_float = 3.14
texto = "Olá, mundo!"
verdadeiro = True
falso = False

# Impressão dos valores das variáveis
print(f"Número inteiro: {numero_inteiro}")
print(f"Número float: {numero_float}")
print(f"Texto: {texto}")
print(f"Verdadeiro: {verdadeiro}")
print(f"Falso: {falso}")
