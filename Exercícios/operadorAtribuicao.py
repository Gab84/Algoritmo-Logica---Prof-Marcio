"""
O script abaixo apresenta exemplos de como utilizar os 
operadores de atribuição =, -=, +=, *=, /= e %= com a variável numero.
"""


# Definindo a variável numero
numero = 10

# Atribuição simples
numero = 20

# Subtração e Atribuição
numero -= 5  # Equivalente a: numero = numero - 5

# Soma e Atribuição
numero += 3  # Equivalente a: numero = numero + 3

# Multiplicação e Atribuição
numero *= 2  # Equivalente a: numero = numero * 2

# Divisão e Atribuição
numero /= 4  # Equivalente a: numero = numero / 4

# Resto da Divisão e Atribuição
numero %= 3  # Equivalente a: numero = numero % 3

# Imprimindo o valor final da variável
print(f"Valor final de numero: {numero}")

